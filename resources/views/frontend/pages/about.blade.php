<h2><b>Introduction</b></h2>
<p>
The International Educational Scientific and Cultural Organization (IESCO) has established in 2016 as an independent non-profit organization based in Malaysia. It aims to support and develop the educational, cultural and scientific sectors of the Arab and Muslim worlds in general and mainly in underprivileged areas. IESCO also strives to strengthen the relationship between Malaysian education institutions and their counterparts in the Arab and Islamic worlds.

The idea is formed from the need to establish a specialized organization dedicated to education, science and culture to support students and educational institutions at less fortunate places. The founders believe that educational and cultural components play an important role in maintaining societies’ identity. They contribute effectively to the intellectual and cultural convergence, and assist in the development of educational, scientific and cultural process in all walks of life. Man, from our point of view – is the target of development and its basic pillar.
</p><br><br>

<h2><b>Vision</b></h2>
<p>To become a leading NGO in providing educational, scientific and cultural programs in less fortunate places.</p> <br><br>

<h2><b>Mission</b></h2>
<p>To enhance educational opportunities to students in less fortunate places. </p><br><br>

<h2><b>Objectives</b></h2>
<p>The organization aims at achieving the following objectives:<br>

1. Create educational opportunities for students in less fortunate places.<br>
2. Encourage scientific research in its various forms to improve well-being of the society.<br>
3. Contribute to building the creative abilities of talented students.<br>
4. Encourage and support development initiatives for graduates in order to produce leaders who will lead various sectors of society. </p><br><br>

<h2><b>Justifications</b></h2>
<p>The establishment of The International Educational, Scientific and Cultural Organization, is based on several factual and logical basis, most important of which are:<br>

1. The collapse of the education sector in target countries due to wars and poverty.<br>
2. The need to support the huge number of students looking for educational opportunities in target countries.<br>
3. The need to empower students in less fortunate places to participate in the development of their own countries.<br>
4. An increasing number of refugees from target countries who need education.<br>
5. The need to improve scientific research in those countries which lack proper facilities and fund.</p><br><br>
 

